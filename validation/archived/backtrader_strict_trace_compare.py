"""Deterministic per-bar trace comparator for Backtrader strict parity.

Compares Backtrader vs ml4t-backtest[backtrader_strict] on:
- cash
- positions snapshot
- order intents generated by canonical target logic
- order acceptance/rejection counters (framework-normalized)
"""

